% Manual User Interface (MUI) package
%
% This package contains customized graphic classes for user interface
% development.
%    Figure     - Minimal figure with a special toolbar
%    Dialog     - Dialog boxes with mixed interface controls
%    DialogPlot - Dialog boxes with plots on the right
%    BasicGUI   - Basic graphical user interface
%    Line       - Lines with adjustable appearance settings 
%    Waitbar    - Graphical progress display
%
% There are also a number of functions within the package
%    setFonts      - Control figure fonts
%    trackProgress - Track serial/parallel loop progress
%
% See also SMASH
%

% Last updated March 23, 2018

% Examples for each class are contained as subpackages of MUI.
%   >> MUI.examples.DialogExampleA  % modal dialog
%   >> MUI.examples.DialogExampleB  % non-modal dialog with large font
%   >> MUI.examples.DialogExampleC  % modal dialog with function callbacks
%   >> MUI.examples.FigureExampleA  % figure with one plot
%   >> MUI.examples.FigureExampleB  % figure with two plots
%   >> MUI.examples.LineExampleA    % two custom lines
%

%


